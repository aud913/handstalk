<div class="well well-sm"><h2 class="white" float="left"> 수화란?</h2></div><br>
<blockquote>
<h5 style='line-height:150%'><img src="/HandsTalk/sign.jpg" style="margin-left:10px;" class="img-rounded" width="200" height="200" align="right" />수화(또는 수어)는 주로 농인(聾人: 보는 사람)과 그의 가족 및 수화통역사 등이 사용하고 있는 언어이다.<br><br> 『한국수화 관련 법령 제정 연구』에서 수화 사용 농인을 대상으로 설문조사를 한 결과 수어(수화)로 사용하기도 한다.

수화의 유형에는 한국수화와 유사수화체계로 구분할 수 있다. 유사수화체계는 농인들이 사용하는 한국수화 이외에 농아동들에게 한국어를 지도하기 위하여 도입한 한글식수화, 문법식수화, 인위적으로 만든 모든 것을 유사수화로 규정하였다.<br><br>

수화는 가장 작은 단위로 언어의 음운론 수준인 수화소(chereme)가 있으며, 수화소에는 손의 모양인 수형(手形, dez), 손의 위치인 수위(手位, tab), 손의 움직임인 수동(手動, sig), 손바닥의 방향인 수향(手向, orientation), 얼굴표정과 몸의 움직임인 비수지신호(非手指信號, nonmanual signals)가 있다.<br><br>

한국수화는 고유수화와 외국으로부터 유입된 수화단어로 구성되어 있고, 수화의 단어와 어휘는 단일 수화와 합성 수화로 구성되어 있으며, 비교적 자유로운 어순으로 표현된다. 따라서 한국수화는 한국어와 다른 음운과 형태, 통사 구조를 가지고 있으며, 언어가 요구하는 필수조건을 모두 갖춘 것으로 눈으로 수화를 인식하고 손을 포함하여 비수지신호와 함께 표현하는 시각 운동체계의 언어이다.</h5>
